import { Platform, TextInput, type TextInputProps } from "react-native";
import { cn } from "@/lib/utils";

function Input({
	className,
	...props
}: TextInputProps & React.RefAttributes<TextInput>) {
	return (
		<TextInput
			placeholderTextColor="hsl(var(--muted-foreground))"
			className={cn(
				"dark:bg-muted/30 border-border/20 bg-background text-foreground flex h-10 w-full min-w-0 flex-row items-center rounded-md border px-3 py-1 text-base leading-5 shadow-sm shadow-black/5 sm:h-9",
				props.editable === false &&
					cn(
						"opacity-50",
						Platform.select({
							web: "disabled:pointer-events-none disabled:cursor-not-allowed",
						}),
					),
				Platform.select({
					web: cn(
						"placeholder:text-muted-foreground selection:bg-primary selection:text-primary-foreground transition-[color,box-shadow] outline-none md:text-sm",
						"focus-visible:border-ring focus-visible:ring-ring/50 focus-visible:ring-[3px]",
						"aria-invalid:ring-destructive/20 dark:aria-invalid:ring-destructive/40 aria-invalid:border-destructive",
					),
					native: "placeholder:text-muted-foreground",
				}),
				className,
			)}
			{...props}
		/>
	);
}

export { Input };
